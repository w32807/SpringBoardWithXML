package com.zerock.aop;

import java.util.Arrays;

import org.aspectj.lang.ProceedingJoinPoint;
import org.aspectj.lang.annotation.*;
import org.springframework.stereotype.Component;

import lombok.extern.log4j.Log4j;

@Aspect
@Log4j
@Component // AOP 클래스도 Bean으로 인식하도록 설정
public class LogAdvice {
	
	// AspectJ의 표현식을 사용 (추후 학습 더 필요)
	// 1. 어떤 위치에 Advice를 적용할 것인지 결정. 
	@Before("execution(* com.zerock.service.SampleService*.*(..))")
	public void logBefore() {
		log.info("==============");
	}
	
	// 특정 메소드를 지정하여 Advice를 적용시킴
	@Before("execution(* com.zerock.service.SampleService*.doAdd(String, String))"
			+ "&& args(str1, str2)")
	public void logBeforeWithParam(String str1, String str2) {
		log.info("str1 >> " + str1);
		log.info("str2 >> " + str2);
	}
	
	@AfterThrowing(pointcut = "execution(* com.zerock.service.SampleService*.*(..))"
			,throwing = "exception")
	public void logException(Exception exception) {
		log.info("Exception.....");
		log.info("Exception >> " + exception);
	}
	
	@Around("execution(* com.zerock.service.SampleService*.*(..))")
	public Object logTime(ProceedingJoinPoint pjp) {
		long start = System.currentTimeMillis();
		log.info("Target >> " + pjp.getTarget());
		log.info("Param >> " + Arrays.toString(pjp.getArgs()));
		
		Object result = null;
		
		try {
			result = pjp.proceed();
		}catch (Throwable e) {
			e.printStackTrace();
		}
		
		long end = System.currentTimeMillis();
		log.info("TIME >> " + (end - start));
		
		return result;
	}
}
